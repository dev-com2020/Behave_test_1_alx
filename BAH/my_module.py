def add(x, y):
    """
    Dodaje dwie liczy
    :param x: (int)liczba pierwsza
    :param y: (int)liczba druga
    :return:  (int)sumę obu liczb

    Example:
    add(2,3)
    5
    add(-2,5)
    3
    """
    return x + y


def multiply(x, y):
    """
    mnożymy dwie liczby
    :param x: (int)liczba pierwsza
    :param y: (int)liczba druga
    :return: (int)wynik obu liczb

    Example:
    multiply(2,2)
    4
    """
    return x * y


def odejmowanie(x, y):
    """

    :param x:
    :param y:
    :return:
    """
    return x - y
